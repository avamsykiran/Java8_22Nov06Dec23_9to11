package com.cts.oopd.app;

public class MathDemoApp {
	public static void main(String[] args) {
		System.out.println(Math.ceil(12.14));
		System.out.println(Math.ceil(12.54));
		System.out.println(Math.round(12.14));
		System.out.println(Math.round(12.54));
		System.out.println(Math.floor(12.14));
		System.out.println(Math.floor(12.54));
	}

}
